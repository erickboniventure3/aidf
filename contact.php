      // Handle form submission status
      document.addEventListener('DOMContentLoaded', function() {
         const urlParams = new URLSearchParams(window.location.search);
         const status = urlParams.get('status');
         const errors = urlParams.get('errors');

         if (status === 'success') {
            showAlert('Thank you! Your message has been sent successfully. We will get back to you soon.', 'success');
         } else if (status === 'email_error') {
            showAlert('Your message was saved, but we encountered an issue sending the confirmation email. Please try again later.', 'warning');
         } else if (status === 'db_error') {
            showAlert('Sorry, there was an error saving your message. Please try again or contact us directly.', 'error');
         } else if (status === 'validation_error' && errors) {
            showAlert('Please correct the following errors: ' + errors, 'error');
         }
      });

      function showAlert(message, type) {
         // Create alert element
         const alertDiv = document.createElement('div');
         alertDiv.className = `alert alert-${type === 'success' ? 'success' : type === 'warning' ? 'warning' : 'danger'} alert-dismissible fade show`;
         alertDiv.style.cssText = 'position: fixed; top: 20px; right: 20px; z-index: 9999; max-width: 400px;';
         alertDiv.innerHTML = `
               ${message}
               <button type="button" class="btn-close" data-bs-dismiss="alert"></button>
            `;

         document.body.appendChild(alertDiv);

         // Auto remove after 5 seconds
         setTimeout(() => {
            if (alertDiv.parentNode) {
               alertDiv.remove();
            }
         }, 5000);
      }

      // Form validation
      document.querySelector('.contact-form form').addEventListener('submit', function(e) {
         const requiredFields = this.querySelectorAll('[required]');
         let isValid = true;

         requiredFields.forEach(field => {
            if (!field.value.trim()) {
               field.style.borderColor = '#dc3545';
               isValid = false;
            } else {
               field.style.borderColor = '#28a745';
            }
         });

         if (!isValid) {
            e.preventDefault();
            showAlert('Please fill in all required fields.', 'error');
         }
      });

      // Reset field border colors on input
      document.querySelectorAll('.form-control').forEach(field => {
         field.addEventListener('input', function() {
            if (this.value.trim()) {
               this.style.borderColor = '#28a745';
            } else {
               this.style.borderColor = '#e9ecef';
            }
         });
      });
   </script>
